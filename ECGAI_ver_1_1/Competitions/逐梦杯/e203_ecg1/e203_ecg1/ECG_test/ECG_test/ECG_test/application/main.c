signed short int FindMax(signed short int b[], unsigned int n, unsigned short int *pMaxPos)
{
    unsigned int i;
	signed short int max;
    max = b[0];             //假设a[0]为最大值
    *pMaxPos = 0;           //假设最大值在数组中的下标位置为0

    for (i=1; i<n; i++)
    {
        if (b[i] > max)
        {
            max = b[i];
            *pMaxPos = i;     //pMaxPos指向最大值数组元素的下标位置
        }
    }
    return max ;
}
